class Flight {
  constructor(
    FlightName,
    FlightDate,
    Origin,
    Destination,
    ScheduledTime,
    DepartureTime
  ) {
    this.FlightName = FlightName;
    this.FlightDate = FlightDate;
    this.Origin = Origin;
    this.Destination = Destination;
    this.ScheduledTime = ScheduledTime;
    this.DepartureTime = DepartureTime;
  }
}
